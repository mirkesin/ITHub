package com.company;

public class CheckOddEven {
    public static void main(String[] args) {
        int number = -2;
        System.out.println("Число равно " + number);

        if (number % 2 == 0) {
            System.out.println("Even Number");
        } else {
            System.out.println("Odd Number");
        }
        System.out.println("bye!");
    }
}
